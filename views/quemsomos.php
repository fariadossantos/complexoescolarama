<h3>Missão, Valores e Objectivos</h3>
<p>
  CEA, visa à formação integral dos alunos, baseando-se nos princípios da solidariedades humana e da integridade.
  Ministrando um ensino de qualidade e promovendo a construção dos conhecimentos, de acordo com diversas etapas do desenvolvimento
  da criança.
</p>
<p>
  O objectivo do Complexo Escolar AMA é preparar os seus alunos para uma perfeita inserção na sociedade, oferecendo-lhes
  possibilidades para o desenvolvimento de suas capacidades intelectuais, cognitivas, afectivas e inter-pessoais, bem
  como da aquisição de vivências de carácter sócio-educativas e cultural.
</p>
